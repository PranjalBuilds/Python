#Ternary Operator with If-else:
a = 10
b = 20

#If-else
if a > b:
    print("a is greater than b")
else: 
    print("b is greater than a")

#Ternary Operator
print('a is larger ' if a > b else 'b is larger')


#Range function
for i in range(10):
    print(i) # 0 to 9

for i in range(1, 10):
    print(i+1)  # 1 to 10

for i in range(1, 10, 2):
    print(i) #1 3 5 7 9